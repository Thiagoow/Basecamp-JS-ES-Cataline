var number = 10.3;
console.log(number);

//Transforma a variável pra String
var v = number.toString();
console.log(v);

// Arredonda pra um valor inteiro pra baixo
var v1 = Math.trunc(number);
console.log(v1);

// Arredonda pra um valor inteiro pra cima
var v2 = Math.ceil(number);
console.log(v2);

// Arredonda pra um valor inteiro mais próximo
var v3 = Math.round(number);
console.log(v3);
